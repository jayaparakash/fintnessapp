const days = ['Monday', 'Tuesday', 'Wednesday', 'Thursday', 'Friday', 'Saturday', 'Sunday']

const day = (theme, exercises, notes = []) => ({ theme, exercises, notes })

const restDay = (notes = ['Rest']) => day('Recovery', [], notes)

const sharedDailyWeek = (title, exercises, notes = []) => {
  const mapping = {}
  for (const weekday of days) {
    mapping[weekday] = weekday === 'Sunday' ? restDay(['Rest']) : day(title, exercises, notes)
  }
  return mapping
}

